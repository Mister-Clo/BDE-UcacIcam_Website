		  <h3 style="font-weight:bold;">Vie étudiante</h3><br/>
		  Le BDE a pour but de défendre les intérêts des élèves en assurant entre autres la liaison entre l’école et les élèves. Ainsi, il participe aux groupes de travail mis en place par l’école pour améliorer la vie étudiante et la scolarité des élèves. Le BDE peut aussi intervenir sur des problèmes plus ponctuels dans la vie des étudiants de l’UCAC-ICAM. Il est également présent au sein des différents conseils (conseils d’administration, pédagogique et scientifique) ainsi qu’aux jurys aux côtés des délégués de promos. Le BDE de l’UCAC-ICAM fait parti du BNEI (Bureau National des Élèves Ingénieurs) qui représente les étudiants ingénieurs au niveau national.
